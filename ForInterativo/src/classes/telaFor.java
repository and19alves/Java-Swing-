       int ini = sliInicio.getValue();
       int fim = sliFim.getValue();
       int passo = sliPasso.getValue();
       int cont;
       
       for(cont=ini;cont<=fim;cont+=passo){
          lista.addElement(cont);
       }
       lstConta.setModel(lista);
       
